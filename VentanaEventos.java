    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
       int filaSeleccionada = tablaEventos.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Por favor selecciona un evento.");
        return;
    }

    // Verificamos si otorga DUTS
    String otorgaDuts = tablaEventos.getValueAt(filaSeleccionada, 1).toString();
    if (!otorgaDuts.equals("Sí")) {
        JOptionPane.showMessageDialog(this, "Este evento no otorga DUTS.");
        return;
    }

    // Obtener los DUTS otorgados
    double dutsEvento = Double.parseDouble(tablaEventos.getValueAt(filaSeleccionada, 2).toString());

    // Sumar los DUTS al usuario
    usuario.agregarDUTS(dutsEvento);

    JOptionPane.showMessageDialog(this, "Te has inscrito correctamente.\nDUTS actuales: " + usuario.getDuts());// TODO add your handling code here:
    }//GEN-LAST:event_btnRegistrarActionPerformed

