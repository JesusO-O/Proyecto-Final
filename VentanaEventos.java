private void cargarEventos() {
    String[] columnas = {"Nombre", "¿Otorga DUTS?", "DUTS Otorgados"};
    DefaultTableModel modelo = new DefaultTableModel(columnas, 0);

    // Supongamos que tienes una lista de eventos (aquí un ejemplo simulado):
    List<modelo.Evento> eventos = new ArrayList<>();
    eventos.add(new modelo.Evento("UTSmart", true, 100));
    eventos.add(new modelo.Evento("CIINATIC", true, 150));
    eventos.add(new modelo.Evento("Grados UTS", false, 0));
    eventos.add(new modelo.Evento("Expobienestar", true, 75));

    for (modelo.Evento e : eventos) {
        Object[] fila = {
            e.getNombre(),
            e.otorgaDuts() ? "Sí" : "No",
            e.getDutsOtorgados()
        };
        modelo.addRow(fila);
    }
    

    tablaEventos.setModel(modelo);
}
